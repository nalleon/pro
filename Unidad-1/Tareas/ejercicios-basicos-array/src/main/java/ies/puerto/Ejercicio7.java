package ies.puerto;

/**
 * Ordena un array de enteros en orden ascendente.
 * @author  Nabil
 */
public class Ejercicio7 {
    public int arrayOrdenado (int [] numeros, int [] resultado){
      for (int i =0; i < numeros.length; i++){
          resultado [i] = numeros [numeros.length-1];
      }
     return 0;
    }
}
